# Question: Create a dictionary that contains the keys a  and b  and their respective values 1  and 2 .

x = {'a': 1}
x['b'] = 2
print(x)    #{'a': 1, 'b': 2}   #my solution

d = {"a": 1, "b": 2}
print(d)    #{"a": 1, "b": 2}   #other solution

d = dict(a = 1, b = 2)
print(d)    #{"a": 1, "b": 2}   #other solution